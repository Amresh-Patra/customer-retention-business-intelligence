WITH temp AS(
SELECT
  customer_id,
  COUNT(order_id) AS total_orders,
  SUM(revenue) AS total_revenue,
  ROUND((SUM(revenue)/NULLIF(COUNT(order_id),0)),2
  )AS avg_order_value
FROM orders
GROUP BY customer_id
ORDER BY total_orders
)
SELECT *,
   CASE
     WHEN total_orders>9 THEN 'Loyal'
     WHEN total_orders>4 THEN 'Moderate'
     ELSE 'One time'
   END AS cust_segment
FROM temp;
-- BUSINESS INTERPRETATION:

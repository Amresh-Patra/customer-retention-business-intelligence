-- Query 3:Regional Performance Dashboard
-- ========

-- OBJECTIVE:
-- Analyze regional business performance based on
-- customers, orders, revenue, and revenue contribution percentage.
WITH temp AS(
 SELECT
  c.region,
  COUNT(o.order_id) AS total_orders,
  COUNT(DISTINCT c.customer_id) AS total_customers,
  SUM(o.revenue) AS total_revenue,
  ROUND((SUM(o.revenue)/NULLIF(COUNT(o.order_id),0)),2) AS avg_order_value
 FROM customers c
 INNER JOIN orders o
 ON c.customer_id = o.customer_id
 GROUP BY c.region
),
lagged AS(
  SELECT *,
    LAG(total_revenue) OVER(ORDER BY total_revenue DESC) AS prev_region
FROM temp
)
SELECT *,
  ROUND(
        (total_revenue/SUM(total_revenue) OVER())*100,2
        ) AS contribution_pct
FROM lagged;
-- BUSINESS INTERPRETATION:
-- The report identifies high-performing regions
-- contributing the highest revenue and customer activity.
-- Businesses can use this analysis to optimize
-- regional marketing strategies and resource allocation.

-- ===========
-- -- QUERY 4:Executive Business Snapshot Dashboard
-- ===========

-- OBJECTIVE:
-- Create a final executive business snapshot dashboard
-- summarizing overall revenue, customer activity,
-- retention performance, and top-performing business areas.
WITH temp AS(
 SELECT
   SUM(revenue) AS total_revenue,
   COUNT(DISTINCT customer_id) AS total_customers,
   COUNT(order_id) AS total_orders,
   ROUND(
        (SUM(revenue)/NULLIF(COUNT(order_id),0)),2
        ) AS avg_order_value
 FROM orders
),
regional AS(
  SELECT 
    region AS best_performing_region
  FROM (
        SELECT
          c.region,
          SUM(o.revenue) AS region_revenue
          FROM customers c
          INNER JOIN orders o
          ON c.customer_id = o.customer_id
          GROUP BY c.region
          ORDER BY SUM(o.revenue) DESC
          LIMIT 1
          ) AS temp2
),
month_revenue AS(
  SELECT
    monthly_revenue AS highest_revenue_month
    FROM (
        SELECT
          DATE_FORMAT(order_date,'%Y-%M') AS month_,
          SUM(revenue) AS monthly_revenue
          FROM orders
          GROUP BY DATE_FORMAT(order_date,'%Y-%M')
          ORDER BY SUM(revenue) DESC
          LIMIT 1
          ) AS temp3
),
repeat_cte AS(
SELECT
    ROUND(
          (COUNT(DISTINCT CASE 
                WHEN order_count>1
                THEN customer_id
                END)/
		  NULLIF(COUNT(customer_id),0))*100,2
          )AS repeat_customer_percentage
	FROM (
		SELECT
        customer_id,
        COUNT(order_date) AS order_count
        FROM orders
        GROUP BY customer_id
        ) AS temp4
)
SELECT *
FROM temp
CROSS JOIN regional
CROSS JOIN month_revenue
CROSS JOIN repeat_cte;
-- BUSINESS INTERPRETATION:
-- The dashboard provides a high-level summary of
-- overall business performance including total revenue,
-- customers, orders, repeat customer percentage,
-- best-performing region, and highest revenue month.
--
-- The strong repeat customer percentage and high revenue
-- indicate effective customer retention, successful
-- marketing campaigns, and strong customer satisfaction.
--
-- Businesses can use this executive snapshot to monitor
-- overall growth performance and support strategic
-- business decision-making.


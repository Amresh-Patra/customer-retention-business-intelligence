-- ===========
-- -- QUERY 1:
-- ===========

-- OBJECTIVE:
-- Analyze acquisition cohorts based on acquisition month.
-- Measure total customers, total orders, and total revenue
-- generated by each acquisition cohort.

SELECT
    c.acquisition_month,
    COUNT(DISTINCT c.customer_id) AS total_customers,
    COUNT(o.order_id) AS total_orders,
    SUM(o.revenue) AS total_revenue
FROM customers c
INNER JOIN orders o
ON c.customer_id = o.customer_id
GROUP BY c.acquisition_month
ORDER BY total_revenue DESC;

-- BUSINESS INTERPRETATION:
-- In the given output, we can observe strong customer
-- acquisition volume across multiple acquisition months,
-- with several cohorts generating very high total revenue
-- and order volume.
--
-- The March-21 cohort generated the highest revenue and
-- total orders, indicating that this acquisition period
-- performed exceptionally well.
--
-- Businesses can use this insight to strategically increase
-- marketing investment, targeted advertisements, and seasonal
-- promotional campaigns during high-performing acquisition
-- periods to maximize customer conversion and revenue.

-- ===========
-- -- QUERY 2:
-- ===========

-- OBJECTIVE:
-- Analyze customer acquisition cohorts including customers
-- who did not place any orders using LEFT JOIN.
-- Identify cohorts with weak customer conversion and
-- low purchasing activity.


-- OBJECTIVE:
-- Analyze monthly revenue performance and
-- measure month-over-month revenue growth trends.
WITH temp AS(
SELECT
  DATE_FORMAT(order_date,'%Y %m') AS month_,
  COUNT(order_date) AS total_orders,
  COUNT(DISTINCT customer_id)AS total_customers,
  SUM(revenue) AS monthly_revenue,
  ROUND((SUM(revenue)/NULLIF(COUNT(order_id),0)),2) AS avg_order_value
FROM orders
GROUP BY DATE_FORMAT(order_date,'%Y %m')
),
prev_month AS(
SELECT *,
  LAG(monthly_revenue) OVER(ORDER BY month_) AS prev_month_revenue
FROM temp
)
SELECT *,
  ROUND(
        ((monthly_revenue-prev_month_revenue)
        /NULLIF(prev_month_revenue,0))*100,2
        ) AS revenue_growth_percentage
FROM prev_month;
-- BUSINESS INTERPRETATION:
-- The report shows strong positive monthly revenue growth
-- and increasing customer activity across multiple periods.
-- Businesses can use this analysis to identify
-- high-performing months and optimize seasonal
-- marketing and promotional strategies.


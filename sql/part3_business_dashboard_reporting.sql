-- =====================================================
-- Churn & Customer Activity Analysis
-- =====================================================

-- ===========
-- -- QUERY 1:
-- ===========

-- OBJECTIVE:
-- Identify customer inactivity and classify customers
-- into Active, At Risk, and Churned segments.
WITH temp AS(
SELECT
  customer_id,
  MAX(order_date) AS last_order_date,
  DATEDIFF(CURDATE(),MAX(order_date)) AS days_since_last_order
FROM orders
GROUP BY customer_id
)
SELECT *,
  CASE
    WHEN days_since_last_order>180 THEN 'churned'
    WHEN days_since_last_order>90  THEN 'At Risk'
    ELSE 'Active' 
  END AS activity_status
FROM temp;
-- BUSINESS INTERPRETATION:
-- Most customers fall under the Churned category,
-- indicating low recent customer activity and weak retention.
-- Businesses can use retention campaigns, personalized offers,
-- and loyalty programs to reduce customer churn.

-- ===========
-- -- QUERY 2:
-- ===========

-- OBJECTIVE:
-- Segment customers based on revenue contribution,
-- order frequency, and inactivity period to identify
-- high-value churn-risk and low-engagement customers.
WITH temp AS(
  SELECT
    customer_id,
    SUM(revenue) AS total_revenue,
    COUNT(order_date) AS total_orders,
    MAX(order_date) AS last_order_date,
    DATEDIFF(CURDATE(),MAX(order_date)) AS days_since_last_order
FROM orders
GROUP BY customer_id
)
SELECt *,
  CASE
    WHEN days_since_last_order>180 AND total_revenue>50000 THEN 'High Value Churn Risk'
    WHEN days_since_last_order>60 AND total_revenue>20000 THEN 'Medium Value AND Risk'
    WHEN days_since_last_order<=60 THEN 'Active Customer'
    WHEN total_orders<10 THEN 'Low Engagement'
  END AS activity_status
FROM temp;
-- BUSINESS INTERPRETATION:
-- Many customers belong to the Low Engagement category,
-- while several customers fall under High Value Churn Risk.
-- Businesses can target these customers using personalized
-- retention strategies, exclusive offers, and reactivation campaigns.


-- BUSINESS PROBLEM:
-- Business stakeholders need a centralized KPI dashboard
-- to monitor overall customer activity, revenue performance,
-- and repeat customer behavior for strategic decision-making.

-- OBJECTIVE:
-- Create an executive-level KPI dashboard summarizing
-- overall business performance, customer retention,
WITH temp AS(
SELECT
  SUM(revenue) AS total_revenue,
  COUNT(order_id) AS total_orders,
  COUNT(DISTINCT customer_id)AS total_customers,
  ROUND(SUM(revenue)/NULLIF(COUNT(order_id),0),2) AS avg_order_value,
  ROUND(SUM(revenue)/NULLIF(COUNT(DISTINCT customer_id),0),2) AS avg_customer_revenue,
  COUNT(DISTINCT CASE
        WHEN customer_id IN (
            SELECT customer_id
            FROM orders
            GROUP BY customer_id
            HAVING COUNT(order_id) > 1
        )
        THEN customer_id
    END) AS repeat_customers
FROM orders
)
SELECT *,
   ROUND((repeat_customers/NULLIF(total_customers,0))*100,2) AS repeat_customer_pct
FROM temp;
-- BUSINESS INTERPRETATION:
-- The dashboard provides a high-level overview of
-- overall business performance including total customers,
-- total orders, total revenue, average order value,
-- and repeat customer percentage.
--
-- A strong repeat customer percentage indicates healthy
-- customer retention and customer engagement performance.
--
-- Businesses can use this dashboard to monitor revenue growth,
-- evaluate customer retention strength, and improve strategic
-- decision-making through targeted marketing and loyalty programs.

